a=10
b=3

hasil = a != b
print(a,'!=',b,'=',hasil)
#operasi besar di bandingkan kanan >
hasil = a > b
print(a,'>',b,'=',hasil)
#operasi kecil di bandingkan kanan <
hasil = a < b
print(a,'<',b,'=',hasil)
#operasi besar atau sama dengan kanan >=
hasil = a >= b
print(a,'>=',b,'=',hasil)
# kecil atau sama dengan kanan <=
hasil = a <= b
print(a,'<=',b,'=',hasil)