#operasi aritmatika
a = 10
b = 3
#operasi tambah +
hasil = a + b
print(a,'+',b,'=',hasil)
#operasi pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)
#operasi perkalian *
hasil = a * b
print(a,'*',b,'=',hasil)
#operasi pembagian /
hasil = a / b
print(a,'/',b,'=',hasil)
#operasi sisa bagi %
hasil = a % b
print(a,'%',b,'=',hasil)
#operasi pemangkatan **
hasil = a ** b
print(a,'**',b,'=',hasil)
#operasi sama dengan ==
hasil = a == b
print(a,'==',b,'=',hasil)
 
#operasi tidak sama dengan !=
hasil = a != b
print(a,'!=',b,'=',hasil)
#operasi besar di bandingkan kanan >
hasil = a > b
print(a,'>',b,'=',hasil)
#operasi kecil di bandingkan kanan <
hasil = a < b
print(a,'<',b,'=',hasil)
#operasi besar atau sama dengan kanan >=
hasil = a >= b
print(a,'>=',b,'=',hasil)
# kecil atau sama dengan kanan <=
hasil = a <= b
print(a,'<=',b,'=',hasil)
#operasi logika and
hasil = a and b
print(a,'and',b,'=',hasil)
#operasi logika or
hasil = a or b
print(a,'or',b,'=',hasil)
#operasi negasi/kebalikan not 
hasil = not a
print(a,'not',b,'=',hasil)
#operasi bitwise and
hasil = a & b
print(a,'&',b,'=',hasil)
#operasi bitwise or
hasil = a | b
print(a,'|',b,'=',hasil)
#operasi bitwise xor
hasil = a ^ b
print(a,'^',b,'=',hasil)
#operasi bitwise i's complement
hasil = ~ b
print(a,'~',b,'=',hasil)
#operasi bitwise left-shift
hasil = a << b
print(a,'<<',b,'=',hasil)
#operasi bitwise right-shift
hasil = a >> b
print(a,'>>',b,'=',hasil)