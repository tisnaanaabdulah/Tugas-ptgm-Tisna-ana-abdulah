#operator Bitwise

a = 6
b = 3

#bitwise and (&)
c = a & b
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai Bitwise and:',c)

#bitwise or (|)
c = a | b
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai Bitwise or:',c)

#bitwise xor (^)
c = a ^ b
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai Bitwise xor:',c)

#bitwise NOT (~)
c = ~a
c = ~b
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai Bitwise not:',c)

#shif right (>>)
c = a >> 2
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai shif right:',c)

#shif left (<<)
c = a << 2
print ('nilai :',a,)
print ('nilai :',b,)
print ('nilai shift left:',c)